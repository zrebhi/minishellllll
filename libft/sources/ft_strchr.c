#include "../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	ptr = (char *)s;
	i = 0;
	while (ptr[i] && ptr[i] != (char)c)
		i++;
	if (ptr[i] == (char)c)
		return (&ptr[i]);
	return (NULL);
}
