void	ft_sort_int_tab(int *tab, int size)
{
	int		x;
	int		i;
	int		temp;

	i = 0;
	x = size - 1;
	while (i < x)
	{
		if (*(tab + i) > *(tab + i + 1))
		{
			temp = *(tab + i);
			*(tab + i) = *(tab + i + 1);
			*(tab + i + 1) = temp;
			i = 0;
		}
		else
		{
			i++;
		}
	}
}
